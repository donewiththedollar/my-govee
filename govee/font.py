def char_bitmap(ch):
    """Return a 5x3 grid (rows of booleans) for a character."""
    rows = FONT.get(ch, FONT.get(ch.upper(), FONT[" "]))
    return [[c != "." for c in row] for row in rows]


def char_columns(ch):
    """Return a list of 3 columns, each a list of 5 booleans (top to bottom)."""
    bm = char_bitmap(ch)
    return [[bm[r][c] for r in range(HEIGHT)] for c in range(WIDTH)]


def available_chars():
    """Return the set of renderable characters."""
    return set(FONT.keys())
