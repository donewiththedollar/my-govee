def char_bitmap(ch):
    rows = FONT.get(ch, FONT.get(ch.upper(), FONT[" "]))
    return [[c != "." for c in row] for row in rows]


def char_columns(ch):
    bm = char_bitmap(ch)
    return [[bm[r][c] for r in range(HEIGHT)] for c in range(WIDTH)]


def available_chars():
    return set(FONT.keys())


def morse_code(ch):
    return MORSE.get(ch.upper())
